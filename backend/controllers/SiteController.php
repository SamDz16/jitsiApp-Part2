    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        return $this->render('index');
    }

    public function actionCreate()
    {
        if(isset($_POST['room_name']))
        {
            $meet = new Meet();
            $meet->room_name = $_POST['room_name'];
//            $meet->real_room_name = $_POST['real_room_name'];
            $meet->password = $_POST['password'];

            $this->db->insert("tlb_meet", $meet);

            return $this->render('index');
        }

        return $this->render('create');
    }

    public function actionMeets()
    {
        // Fetch meets from database using ActiveRecords
        // Meet is the model
        $meets = Meet::find()->all();

        // Render the view
        return $this->render('meets', ['meets' => $meets]);
    }

    /**
     * Login action.
     *
     * @return string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $this->layout = 'blank';

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            $model->password = '';

            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Logout action.
     *
     * @return string
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
}
